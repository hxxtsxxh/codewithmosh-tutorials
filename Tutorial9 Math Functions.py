import math

x = 2.9

# the round function tells the number to be rounded to the nearest whole number.
print(round(x))
# the abs function outputs the absolute value of the number.
print(abs(-x))
# math.ceil will bring out the ceiling number of 2.9 which is 3
print(math.ceil(x))
# the math.floor will tell you the floor number or the number closest to it from the bottom of the number scale
print(math.floor(x))
