# in Python, we have two types of numbers: integers and floats.
# we can add two integers by the following
print(10 + 3)
# next is subtraction
print(10 - 3)
# then multiplication
print(10 * 3)
# However, we have two types of division.
print(10 / 3)
# ^this type gives the exact value, the float value
print(10 // 3)
# ^this type rounds the value, the int value

# we also have another operator:
print(10 % 3)
# this operator prints out the remainder of the problem.

# if we want to do exponents, we do two "*".
print(10 ** 3)
# the above should give us the answer of 1000.
